      int dem = 0;

    public void run() {
        while (true) {
            dem++;
            probar.setMinimum(0);
            probar.setMaximum(100);
            probar.setValue(dem);
           
            lblPhantram.setText(dem + "%");
            if(dem == 80){
                lblText.setText("Done!");
            }
            if (dem == 100) {
                new LoginFrm().setVisible(true);
               this.setVisible(false);
            }
            try {
                Thread.sleep(70);
            } catch (Exception e) {
                e.printStackTrace();
            }
        }
    }

